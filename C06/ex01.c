#include <unistd.h>

int	main(int ac, char **av)
{
	int	i;
	int	k;
	int	j;

	k = 1;
	while (--ac)
	{
		i = 0;
		j = 0;
		while (av[k][j])
			j++;
		write(1, av[k], j);
		write(1, "\n", 1);
		k++;
	}
	return (0);
}
